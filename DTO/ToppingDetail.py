class ToppingDetail:
    pizza_id = ""
    topping_id = ""
    
    def getPizzaId(self):
        return self.pizza_id
    def getToppingId(self):
        return self.topping_id
    
    def setPizzaId(self,pizza_id):
        self.pizza_id = pizza_id
    def setToppingId(self,topping_id):
        self.topping_id = topping_id  