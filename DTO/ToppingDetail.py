class ToppingDetail:
    pizzaId = ""
    toppingId = ""
    
    def getPizzaId(self):
        return self.pizzaId
    def getToppingId(self):
        return self.toppingId
    
    def setPizzaId(self,pizzaId):
        self.pizzaId = pizzaId
    def setToppingId(self,toppingId):
        self.toppingId = toppingId  