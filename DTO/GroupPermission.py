class GroupPermission:
    id = ""
    groupId = ""
    permission = ""
    value = ""
    
    def getId(self):
        return self.id
    
    def setId(self, id):
        self.id = id
    
    def getGroupId(self):
        return self.groupId
    
    def setGroupId(self, GroupId):
        self.groupId = GroupId
    
    def getPermission(self):
        return self.permission
    
    def setPermission(self, permission):
        self.permission = permission

    def getValue(self):
        return self.value
    
    def setValue(self, value):
        self.value = value
    
    